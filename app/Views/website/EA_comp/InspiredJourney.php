<script>
    let inspiredCurrentIdx = 0;
    const inspiredTrack = document.getElementById('inspiredTrack');
    const inspiredSlides = Array.from(inspiredTrack.children);
    const inspiredDotsContainer = document.getElementById('inspiredDots');
    let inspiredVisibleCount = 3;

    function updateInspiredDimensions() {
        const width = window.innerWidth;
        if (width >= 1024) {
            inspiredVisibleCount = 3;
        } else if (width >= 640) {
            inspiredVisibleCount = 2;
        } else {
            inspiredVisibleCount = 1;
        }
        setupInspiredDots();
        moveInspiredSlide(0);
    }

    function setupInspiredDots() {
        inspiredDotsContainer.innerHTML = '';
        const dotsCount = inspiredSlides.length - inspiredVisibleCount + 1;
        if (dotsCount <= 1) return;
        
        for (let i = 0; i < dotsCount; i++) {
            const dot = document.createElement('button');
            dot.className = `w-2.5 h-2.5 rounded-full transition-all duration-300 bg-gray-300 ${i === inspiredCurrentIdx ? 'bg-[#5751E1] w-6' : ''}`;
            dot.setAttribute('onclick', `moveInspiredSlide(${i})`);
            inspiredDotsContainer.appendChild(dot);
        }
    }

    function moveInspiredSlide(index) {
        const maxIndex = inspiredSlides.length - inspiredVisibleCount;
        let targetIdx = Math.max(0, Math.min(index, maxIndex));
        inspiredCurrentIdx = targetIdx;
        
        // Calculate gap and slide widths
        const slideWidth = inspiredSlides[0].getBoundingClientRect().width;
        const gap = 24; // gap-6 is 24px
        
        const offset = targetIdx * (slideWidth + gap);
        inspiredTrack.style.transform = `translateX(-${offset}px)`;
        
        // Update dots UI
        const dots = Array.from(inspiredDotsContainer.children);
        dots.forEach((dot, idx) => {
            if (idx === targetIdx) {
                dot.classList.remove('bg-gray-300', 'w-2.5');
                dot.classList.add('bg-[#5751E1]', 'w-6');
            } else {
                dot.classList.remove('bg-[#5751E1]', 'w-6');
                dot.classList.add('bg-gray-300', 'w-2.5');
            }
        });
    }

    function nextInspiredSlide() {
        const maxIndex = inspiredSlides.length - inspiredVisibleCount;
        if (inspiredCurrentIdx >= maxIndex) {
            moveInspiredSlide(0); // Loop back
        } else {
            moveInspiredSlide(inspiredCurrentIdx + 1);
        }
    }

    function prevInspiredSlide() {
        const maxIndex = inspiredSlides.length - inspiredVisibleCount;
        if (inspiredCurrentIdx <= 0) {
            moveInspiredSlide(maxIndex); // Loop back to end
        } else {
            moveInspiredSlide(inspiredCurrentIdx - 1);
        }
    }

    // Lightbox Controls
    function openInspiredLightbox(imgSrc) {
        const lightbox = document.getElementById('inspiredLightbox');
        const img = document.getElementById('inspiredLightboxImg');
        const container = lightbox.querySelector('div');
        img.src = imgSrc;
        lightbox.classList.remove('hidden');
        lightbox.classList.add('flex');
        setTimeout(() => {
            lightbox.classList.remove('opacity-0');
            container.classList.remove('scale-95');
        }, 10);
        document.body.style.overflow = 'hidden';
    }

    function closeInspiredLightbox() {
        const lightbox = document.getElementById('inspiredLightbox');
        const container = lightbox.querySelector('div');
        lightbox.classList.add('opacity-0');
        container.classList.add('scale-95');
        setTimeout(() => {
            lightbox.classList.remove('flex');
            lightbox.classList.add('hidden');
        }, 300);
        document.body.style.overflow = '';
    }

    // Init & Listeners
    window.addEventListener('resize', updateInspiredDimensions);
    document.addEventListener('DOMContentLoaded', () => {
        // Run initial dimensions layout
        setTimeout(updateInspiredDimensions, 100);
        
        // Touch events support
        let startX = 0;
        let isDragging = false;
        
        inspiredTrack.addEventListener('touchstart', (e) => {
            startX = e.touches[0].clientX;
            isDragging = true;
        });
        
        inspiredTrack.addEventListener('touchend', (e) => {
            if (!isDragging) return;
            const diffX = e.changedTouches[0].clientX - startX;
            isDragging = false;
            
            if (diffX > 50) {
                prevInspiredSlide();
            } else if (diffX < -50) {
                nextInspiredSlide();
            }
        });
    });
</script>
